function languageUrl(originalUrl, lang) {
  const url = new URL(originalUrl || '/', 'http://local');
  url.searchParams.set('lang', lang);
  return `${url.pathname}${url.search}`;
}

function i18n(req, res, next) {
  const requested = req.query.lang;
  if (requested === 'en' || requested === 'zh') {
    req.session.lang = requested;
  }

  const lang = req.session.lang || 'zh';
  res.locals.lang = lang;
  res.locals.otherLang = lang === 'zh' ? 'en' : 'zh';
  res.locals.languageSwitchUrl = languageUrl(req.originalUrl, res.locals.otherLang);
  res.locals.t = (key) => {
    const dict = dictionaries[lang] || dictionaries.zh;
    return dict[key] || dictionaries.en[key] || key;
  };
  res.locals.tFlash = (message) => {
    if (lang === 'en') return message;
    return (flashMessages[message] && flashMessages[message][lang]) || message;
  };
  res.locals.formatDate = (value) => new Date(value).toLocaleDateString(lang === 'zh' ? 'zh-CN' : 'en-US', {
    month: 'short',
    day: 'numeric',
    year: 'numeric'
  });
  next();
}

module.exports = i18n;
